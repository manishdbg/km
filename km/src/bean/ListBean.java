package bean;

public class ListBean {

	 private String prod_name;
	 private String prod_rel_id;
	 private String proc_name;
	 private String proc_rel_id;
	 private String rule_name;
	 private String scenario_name;
		public ListBean() {
	    
		}
	 
	public String getProd_name() {
		return prod_name;
	}
	public void setProd_name(String prod_name) {
		this.prod_name = prod_name;
	}
	public String getProd_relid() {
		return prod_rel_id;
	}
	public void setProd_relid(String prod_rel_id) {
		this.prod_rel_id = prod_rel_id;
	}
	public String getProc_name() {
		return proc_name;
	}
	public void setProc_name(String proc_name) {
		this.proc_name = proc_name;
	}
	public String getProc_relid() {
		return proc_rel_id;
	}
	public void setProc_relid(String proc_rel_id) {
		this.proc_rel_id = proc_rel_id;
	}
	public String getRule_name() {
		return rule_name;
	}
	public void setRule_name(String rule_name) {
		this.rule_name = rule_name;
	}
	public String getScenario_name() {
		return scenario_name;
	}
	public void setScenario_name(String scenario_name) {
		this.scenario_name = scenario_name;
	}
	 
	 
	 
}